function arrayInterSection(arr1, arr2) {
  return arr1.filter((value) => arr2.includes(value));
}
console.log(arrayInterSection([1, 2, 3], [2, 3, 4]));

function intersection(arr1, arr2) {
  return arr1.filter((value) => arr2.includes(value));
}
console.log(intersection([2, 3, 4], [3, 4, 5]));

